 function getCookieExpirationString() {
   var now,
     year,
     month,
     day,
     hour,
     minute,
     second,
     millisecond,
     nextMonth;

   // calculate the date
   now = new Date();
   year = now.getFullYear();
   month = now.getMonth();
   day = now.getDate();
   hour = now.getHours();
   minute = now.getMinutes();
   second = now.getSeconds();
   millisecond = now.getMilliseconds();

   // derive nextMonth, accounting for december
   if (month == 11) {
     // nextMonth is january of the next year
     nextMonth = new Date(year+1, 0, day, hour, minute, second, millisecond);
   } else {
     // nextMonth is next month
     nextMonth = new Date(year, month+1, day, hour, minute, second, millisecond);
   }
   return nextMonth.toGMTString();
 }

