 function getViewportProperty(propertyName) {
   var viewportTag = document.head.querySelector('meta[name="viewport"]');
   var properties = viewportTag.content.split(",");
   for (var i = 0; i < properties.length ; i++) {
     if (properties[i].match(propertyName)) {
       return properties[i].split("=")[1];
     }
   }
 }

 function getInitialScale() {
   var initialScale = getViewportProperty("initial-scale");
   if (initialScale) {
     initialScale = parseFloat(initialScale, 10);
   }
   return initialScale;
 }

 function userScalable() {
   var scalableValue = getViewportProperty("user-scalable");
   var scalable = true;  // default scalability is true;

   if (scalableValue) {
     if (scalableValue.toLowerCase() === "no") {
       scalable = false;
     }
   }
   return scalable;
 }

 function viewportZoomOverride() {
   if (getInitialScale() === 1 && !userScalable()) {
     return true;
   }
   return false;
 }

 function getCurrentZoom() {
   var viewportWidth = document.documentElement.clientWidth;
   var screenWidth = window.innerWidth;
   var viewportScale = 1;

   if (!viewportZoomOverride()) {
     viewportScale = viewportWidth / screenWidth;
     viewportScale = parseFloat(viewportScale, 10);
   }
   else {
     viewportScale = 3.2;
   }
   return viewportScale;
 }

 function responsitionFixedElement(element) {
   var currentZoom = getCurrentZoom();
   var pageOffset = {x:window.pageXOffset, y:window.pageYOffset};
   var statusBarHeight = 20 / currentZoom;
   var elementOffset = {x:0, y:0};
   elementOffset.y = document.body.scrollHeight - window.innerHeight - pageOffset.y - statusBarHeight;


   var currentTransform = new WebKitCSSMatrix();
   currentTransform = currentTransform.scale(1/currentZoom);

   element.style.webkitTransform = currentTransform;

   element.style.bottom = elementOffset.y + "px";
   element.style.left = pageOffset.x + "px";

 }

 function resizeOptInLink(optInLink) {

   if (!hasFixedPositioning()) {
     responsitionFixedElement(optInLink);
   }
   else {
     // should be zoomed at 1 regardless of the content
     var currentZoom = getCurrentZoom();
     optInLink.style.zoom = 1/currentZoom;
   }
 }

